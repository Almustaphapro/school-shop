// Helper function to get students by class
export const getStudentsByClass = (className) => {
  return studentData[className] || [];
};

// Helper function to search students across all classes
export const searchStudents = (query) => {
  if (!query) return [];
  
  const allStudents = Object.values(studentData).flat();
  return allStudents.filter(student => 
    student.studentId.toLowerCase().includes(query.toLowerCase()) ||
    student.name.toLowerCase().includes(query.toLowerCase())
  );
};

// Helper function to get all class names
export const getAllClassNames = () => {
  return Object.keys(studentData);
};

// Helper function to get student count per class
export const getStudentCountPerClass = () => {
  const counts = {};
  Object.keys(studentData).forEach(className => {
    counts[className] = studentData[className].length;
  });
  return counts;
};

